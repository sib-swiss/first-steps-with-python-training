
# The % operator returns the remainder of the Euclidean division of two
# numbers.
# A number x is evenly divisible by y if the remainder of the Euclidean
# division is equal to 0, or, to put it in semi-code: x % y is equal to 0.
#
# We then use the "==" comparison operator to return a boolean value (True
# or False).

is_divisible = (7453102041884640481897 % 135138672131) == 0
print(is_divisible)
